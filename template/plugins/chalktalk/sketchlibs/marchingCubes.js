   function E(n, v, e) { return 3 * (n + (v & 1) + (v>>1 & 1) * dj + (v>>2 & 1) * dk) + e; }
   var nk = V.length / (ni * nj), dj = ni, dk = ni * nj, P = [], T = [], I = [];
   for (let k = 0 ; k < nj - 1 ; k++)
   for (let j = 0 ; j < nj - 1 ; j++) {
      let n = j * dj + k * dk;
      for (let i = 0 ; i < ni - 1 ; i++, n++) {
         let C = this.data[ Z(n   )      | Z(n+1   ) << 1 | Z(n+dj   ) << 2 | Z(n+1+dj   ) << 3 |
                            Z(n+dk) << 4 | Z(n+1+dk) << 5 | Z(n+dj+dk) << 6 | Z(n+1+dj+dk) << 7 ];
         for (let m = 0 ; m < C.length ; m += 6) {
            let a = E(n, C[m  ], C[m+1]),
                b = E(n, C[m+2], C[m+3]),
                c = E(n, C[m+4], C[m+5]);
            T.push(a, b, c);
            I[a] = I[b] = I[c] = 1;
         }
      }
   }
   for (let v = 0 ; v < 3 * V.length ; v++)
      if (I[v]) {
         I[v] = P.length / 3;
         let d = v % 3, n = (v - d) / 3, dn = d==0 ? 1 : d==1 ? dj : dk,
             t = (z - V[n]) / (V[n + dn] - V[n]),
             i = n % ni, j = Math.floor(n / dj) % nj, k = Math.floor(n / dk);
         P.push((d==0 ? i+t : i)/ni, (d==1 ? j+t : j)/ni, (d==2 ? k+t : k)/ni);
      }
   for (let n = 0 ; n < T.length ; n++)
      T[n] = I[T[n]];
   return [P, T];
